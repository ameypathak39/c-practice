#include<stdio.h>
int main(){
    int myInt = 25;
    float myFloat = 3.14;
    double myDouble = 9.87654321;
    char myChar = 'A';
    char myString[] = "hello, amey";
    printf("Integer :%d\n", myInt);
    printf("Float :%2f\n", myFloat);
    printf("Double :%8lf\n",myDouble);
    printf("character : %c\n",myChar);
    printf("String :%s\n",myString);
    
    return 0;
}
